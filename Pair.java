/**
 * Pair is a utility class that stores 2 numbers. 
 * <p>
 * Used for coordinates in the world's tile system.
 * 
 * @author Edwin
 * @version 1
 */
public class Pair  
{
    /**
     * Constructor for objects of class Pair
     */
    public int f, s;
    public Pair(int first, int second) {
       f = first; s = second; // stores 2 numbers
    } 
}
